package arrays;

public class ArrayExample {
    public static void main(String[] args) {

        int[] x = new int[6];
        x[0] = 5;
        x[1] = 4;
        x[2] = 8;
        x[3] = 19;
        x[4] = 50;
        x[5] = 1;

        for (int i = 0; i < 6; i++) {
            System.out.println(x[i]);
        }
    }
}
