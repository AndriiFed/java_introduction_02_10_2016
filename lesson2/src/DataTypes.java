public class DataTypes {
    public static void main(String[] args) {

        String str = "любое слово или текст"; //str название, может быть любым

        byte var = -128; //-128..127 = 2^8 | 1 byte
        short varShort = 345; //-32768..32767 = 2^16 | 2 byte
        int varInt = 150000; //-2^31..2^31-1 | 4 byte
        long varLong = 12342134; // 8 byte

        float varFloat = 25.56f; // 4 byte
        double varDouble = 345.56; // 8 byte

    }
}
