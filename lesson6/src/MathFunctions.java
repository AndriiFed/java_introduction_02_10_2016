public class MathFunctions {
    public static void main(String[] args) {

        int number = -10;

        int abs = Math.abs(number);
        double power = Math.pow(5, 3); //5 ^ 3
        double cos = Math.cos(0);
        int squreRoot = (int) Math.sqrt(9);

        System.out.println("Absolute value: " + abs);
        System.out.println("5 в степени 3: " + power);
        System.out.println("Косинус: " + cos);
        System.out.println("Корень числа: " + squreRoot);
    }
}
